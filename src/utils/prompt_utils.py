def get_english_prompt_template() -> str:
    return ""


def get_prompt(language: str) -> str:
    if language == "he":
        return get_hebrew_prompt_template()
    elif language == "en":
        return get_english_prompt_template()
    else:
        raise ValueError(f"Invalid langauge: {language}")


